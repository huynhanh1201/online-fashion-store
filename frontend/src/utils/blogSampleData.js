// Function để tạo blog data với thông tin đầy đủ
export const createBlogData = (blogInput) => {
  const now = new Date().toISOString()
  
  // Tạo slug từ title
  const slug = blogInput.title
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim('-')

  return {
    title: blogInput.title,
    slug: slug,
    excerpt: blogInput.excerpt,
    content: blogInput.content,
    coverImage: blogInput.coverImage,
    images: blogInput.images || [],
    author: {
      name: 'Admin User',
      avatar: 'https://images.unsplash.com/photo-1472099645785-5658abf4ff4e?w=100&h=100&fit=crop&crop=face',
      id: 'admin-user-id'
    },
    tags: blogInput.tags || [],
    category: blogInput.category,
    brand: blogInput.brand || '',
    publishedAt: blogInput.status === 'published' ? now : null,
    updatedAt: now,
    status: blogInput.status || 'draft',
    meta: {
      title: blogInput.meta?.title || blogInput.title,
      description: blogInput.meta?.description || blogInput.excerpt,
      keywords: blogInput.meta?.keywords || []
    },
    views: Math.floor(Math.random() * 1000) + 100, // Random views for demo
    likes: Math.floor(Math.random() * 100) + 10,   // Random likes for demo
    destroy: false,
    commentsCount: Math.floor(Math.random() * 20)  // Random comments for demo
  }
}

